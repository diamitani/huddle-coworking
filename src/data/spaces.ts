export const cities = [...new Set(spaces.map(s => s.city))].sort()
export const states = [...new Set(spaces.map(s => s.state))].sort()
export const allAmenities = [...new Set(spaces.flatMap(s => s.amenities))].sort()
export const allTypes = [...new Set(spaces.map(s => s.type))].sort()

export function getSpaceBySlug(slug: string): CoworkingSpace | undefined {
  return spaces.find(s => s.slug === slug)
}

export function getSpacesByCity(city: string): CoworkingSpace[] {
  return spaces.filter(s => s.city.toLowerCase() === city.toLowerCase())
}

export function getFeaturedSpaces(): CoworkingSpace[] {
  return spaces.filter(s => s.featured)
}

export function getPopularSpaces(): CoworkingSpace[] {
  return spaces.filter(s => s.popular)
}
